class Solution(object):
    def isPalindrome(self, x):
        """
        :type x: int
        :rtype: bool
        """
        if x < 0:
            return False
        s = x
        t = 0
        while s > 0:
            t *= 10
            t += s % 10
            s /= 10
        return x == t
